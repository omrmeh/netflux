DROP TABLE if exists FILM;
DROP TABLE if exists GENRE;

Create Table GENRE (
ID_GENRE SERIAL PRIMARY KEY,
G_NAME varchar (25));

Create Table FILM (
ID_FILM SERIAL PRIMARY KEY,
ID_GENRE int,
F_TITLE varchar (100),
F_YEAR int, 
F_RATINGS int, 
F_POSTER varchar (300), 
F_SYNOPSIS text,
F_LENGTH int);
